    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        View v = inflater.inflate(R.layout.profile_fragment, container, false);
        FirebaseUser user = FirebaseAuth.getInstance().getCurrentUser();
        if (user != null) {
            // Name, email address, and profile photo Url
            String name = user.getDisplayName();
            String email = user.getEmail();
            Uri photoUrl = user.getPhotoUrl();
            String uid = user.getUid();
            TextView nameText = v.findViewById(R.id.name);
            TextView emailText = v.findViewById(R.id.email);
            nameText.setText(name);
            emailText.setText(email);
            ImageView imageView = v.findViewById(R.id.pimag);
            Log.i("iam", "onCreateView: "+photoUrl.toString());
            new DownloadImageTask(imageView).execute(photoUrl.toString());

        }

        return v;
    }
    public static Drawable LoadImageFromWebOperations(String url) {
        try {
            InputStream is = (InputStream) new URL(url).getContent();
            Drawable d = Drawable.createFromStream(is, "src name");
            return d;
        } catch (Exception e) {
            return null;
        }
    }
